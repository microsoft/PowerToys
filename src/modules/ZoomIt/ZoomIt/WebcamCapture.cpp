//----------------------------------------------------------------------------
// WebcamCapture::WebcamCapture
//----------------------------------------------------------------------------
WebcamCapture::WebcamCapture(
    winrt::com_ptr<ID3D11Device> const& device,
    winrt::com_ptr<ID3D11DeviceContext> const& context,
    const wchar_t* deviceSymLink,
    UINT outputWidth,
    UINT outputHeight,
    Position position,
    Size size,
    Shape shape,
    bool fullScreenRecording,
    WebcamBackgroundMode backgroundMode,
    const wchar_t* backgroundImagePath,
    int brightness )
    : m_d3dDevice( device )
    , m_d3dContext( context )
    , m_deviceSymLink( deviceSymLink ? deviceSymLink : L"" )
    , m_outputWidth( outputWidth )
    , m_outputHeight( outputHeight )
    , m_position( position )
    , m_size( size )
    , m_shape( shape )
    , m_fullScreenRecording( fullScreenRecording )
    , m_backgroundMode( backgroundMode )
    , m_backgroundImagePath( backgroundImagePath ? backgroundImagePath : L"" )
    , m_brightness( brightness )
{
    // Build the gamma LUT from the user's brightness setting.
    // brightness 0 → gamma 2.0 (dark), 50 → gamma 1.0 (neutral), 100 → gamma 0.5 (bright).
    m_lutGamma = pow( 2.0, ( 50 - m_brightness ) / 50.0 );
    for( int i = 0; i < 256; i++ )
        m_gammaLUT[i] = static_cast<uint8_t>(
            255.0 * pow( i / 255.0, m_lutGamma ) + 0.5 );
}

//----------------------------------------------------------------------------
// WebcamCapture::~WebcamCapture
//----------------------------------------------------------------------------
WebcamCapture::~WebcamCapture()
{
    Stop();
}

//----------------------------------------------------------------------------
// WebcamCapture::InitGpuComposite
//
// Creates a dedicated D3D11 device for GPU-accelerated compositing in
// the capture thread.  This avoids contention with the recording
// session's device/context.  Returns false if GPU compositing is not
// available (falls back to CPU).
//----------------------------------------------------------------------------
bool WebcamCapture::InitGpuComposite()
{
    UINT flags = D3D11_CREATE_DEVICE_BGRA_SUPPORT;
#if _DEBUG
    flags |= D3D11_CREATE_DEVICE_DEBUG;
#endif
    D3D_FEATURE_LEVEL levels[] = {
        D3D_FEATURE_LEVEL_11_1,
        D3D_FEATURE_LEVEL_11_0,
    };
    D3D_FEATURE_LEVEL levelCreated;

    HRESULT hr = D3D11CreateDevice(
        nullptr, D3D_DRIVER_TYPE_HARDWARE, nullptr,
        flags, levels, ARRAYSIZE( levels ),
        D3D11_SDK_VERSION,
        m_gpuDevice.put(), &levelCreated, m_gpuContext.put() );

    if( FAILED( hr ) )
    {
        // Retry without debug layer.
        flags &= ~D3D11_CREATE_DEVICE_DEBUG;
        hr = D3D11CreateDevice(
            nullptr, D3D_DRIVER_TYPE_HARDWARE, nullptr,
            flags, levels, ARRAYSIZE( levels ),
            D3D11_SDK_VERSION,
            m_gpuDevice.put(), &levelCreated, m_gpuContext.put() );
    }
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU composite: D3D11CreateDevice failed\n" );
        return false;
    }

    // Create vertex shader.
    hr = m_gpuDevice->CreateVertexShader(
        g_WebcamCompositeVS, sizeof( g_WebcamCompositeVS ), nullptr, m_compositeVS.put() );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU composite: CreateVertexShader failed\n" );
        return false;
    }

    // Create pixel shader.
    hr = m_gpuDevice->CreatePixelShader(
        g_WebcamCompositePS, sizeof( g_WebcamCompositePS ), nullptr, m_compositePS.put() );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU composite: CreatePixelShader failed\n" );
        return false;
    }

    // Create constant buffer.
    D3D11_BUFFER_DESC cbDesc = {};
    cbDesc.ByteWidth = sizeof( GpuCompositeConstants );
    cbDesc.Usage = D3D11_USAGE_DYNAMIC;
    cbDesc.BindFlags = D3D11_BIND_CONSTANT_BUFFER;
    cbDesc.CPUAccessFlags = D3D11_CPU_ACCESS_WRITE;
    hr = m_gpuDevice->CreateBuffer( &cbDesc, nullptr, m_compositeCB.put() );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU composite: CreateBuffer(CB) failed\n" );
        return false;
    }

    // Create bilinear sampler with clamp addressing.
    D3D11_SAMPLER_DESC sampDesc = {};
    sampDesc.Filter = D3D11_FILTER_MIN_MAG_MIP_LINEAR;
    sampDesc.AddressU = D3D11_TEXTURE_ADDRESS_CLAMP;
    sampDesc.AddressV = D3D11_TEXTURE_ADDRESS_CLAMP;
    sampDesc.AddressW = D3D11_TEXTURE_ADDRESS_CLAMP;
    hr = m_gpuDevice->CreateSamplerState( &sampDesc, m_bilinearSampler.put() );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU composite: CreateSamplerState failed\n" );
        return false;
    }

    // Rasterizer: no culling, solid fill.
    D3D11_RASTERIZER_DESC rasDesc = {};
    rasDesc.FillMode = D3D11_FILL_SOLID;
    rasDesc.CullMode = D3D11_CULL_NONE;
    rasDesc.DepthClipEnable = TRUE;
    hr = m_gpuDevice->CreateRasterizerState( &rasDesc, m_gpuRasterState.put() );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU composite: CreateRasterizerState failed\n" );
        return false;
    }

    // Blend: disabled (write all channels, no blending).
    D3D11_BLEND_DESC blendDesc = {};
    blendDesc.RenderTarget[0].RenderTargetWriteMask = D3D11_COLOR_WRITE_ENABLE_ALL;
    hr = m_gpuDevice->CreateBlendState( &blendDesc, m_gpuBlendState.put() );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU composite: CreateBlendState failed\n" );
        return false;
    }

    OutputDebug( L"[WebcamCapture] GPU composite: initialized (FL %x)\n", levelCreated );

    // ── GPU box-blur compute shader ────────────────────────────────
    hr = m_gpuDevice->CreateComputeShader(
        g_BoxBlurCS, sizeof( g_BoxBlurCS ), nullptr, m_blurCS.put() );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU blur: CreateComputeShader failed (0x%08X)\n", hr );
        // Non-fatal: GPU composite still works, blur stays on CPU.
    }
    else
    {
        // Verify B8G8R8A8_UNORM supports typed UAV stores on this GPU.
        // Required for the compute shader to write blur results.
        UINT fmtSupport2 = 0;
        hr = m_gpuDevice->CheckFormatSupport( DXGI_FORMAT_B8G8R8A8_UNORM, &fmtSupport2 );
        bool uavSupported = SUCCEEDED( hr ) &&
            ( fmtSupport2 & D3D11_FORMAT_SUPPORT_TYPED_UNORDERED_ACCESS_VIEW );

        if( !uavSupported )
        {
            OutputDebug( L"[WebcamCapture] GPU blur: B8G8R8A8_UNORM UAV not supported\n" );
            m_blurCS = nullptr;
        }
        else
        {
            // Create constant buffer for blur parameters.
            D3D11_BUFFER_DESC blurCbDesc = {};
            blurCbDesc.ByteWidth = sizeof( GpuBlurConstants );
            blurCbDesc.Usage = D3D11_USAGE_DYNAMIC;
            blurCbDesc.BindFlags = D3D11_BIND_CONSTANT_BUFFER;
            blurCbDesc.CPUAccessFlags = D3D11_CPU_ACCESS_WRITE;
            hr = m_gpuDevice->CreateBuffer( &blurCbDesc, nullptr, m_blurCB.put() );
            if( FAILED( hr ) )
            {
                OutputDebug( L"[WebcamCapture] GPU blur: CreateBuffer(CB) failed\n" );
                m_blurCS = nullptr;
            }
            else
            {
                m_gpuBlurReady = true;
                OutputDebug( L"[WebcamCapture] GPU blur: compute shader initialized\n" );
            }
        }
    }

    return true;
}

//----------------------------------------------------------------------------
// WebcamCapture::GpuBoxBlur
//
// Runs 4 compute-shader dispatches (H→V→H→V) to approximate a Gaussian
// blur.  The result stays GPU-resident in m_blurPingPong[0] and can be
// used directly as the BlurTex SRV in GpuComposite, skipping the CPU
// blur entirely.  Returns true on success.
//----------------------------------------------------------------------------
bool WebcamCapture::GpuBoxBlur(
    const UINT32* pixels, UINT width, UINT height, int radius )
{
    if( !m_gpuBlurReady || !pixels || width == 0 || height == 0 )
        return false;

    HRESULT hr;

    // ── Create / resize ping-pong textures if needed ───────────────
    if( m_blurPPW != width || m_blurPPH != height )
    {
        for( int i = 0; i < 2; i++ )
        {
            m_blurPingUAV[i] = nullptr;
            m_blurPingSRV[i] = nullptr;
            m_blurPingPong[i] = nullptr;

            D3D11_TEXTURE2D_DESC td = {};
            td.Width = width;
            td.Height = height;
            td.MipLevels = 1;
            td.ArraySize = 1;
            td.Format = DXGI_FORMAT_B8G8R8A8_UNORM;
            td.SampleDesc.Count = 1;
            td.Usage = D3D11_USAGE_DEFAULT;
            td.BindFlags = D3D11_BIND_SHADER_RESOURCE | D3D11_BIND_UNORDERED_ACCESS;
            hr = m_gpuDevice->CreateTexture2D( &td, nullptr, m_blurPingPong[i].put() );
            if( FAILED( hr ) ) return false;

            hr = m_gpuDevice->CreateShaderResourceView(
                m_blurPingPong[i].get(), nullptr, m_blurPingSRV[i].put() );
            if( FAILED( hr ) ) return false;

            hr = m_gpuDevice->CreateUnorderedAccessView(
                m_blurPingPong[i].get(), nullptr, m_blurPingUAV[i].put() );
            if( FAILED( hr ) ) return false;
        }
        m_blurPPW = width;
        m_blurPPH = height;
    }

    // ── Upload source pixels into ping-pong[0] ────────────────────
    m_gpuContext->UpdateSubresource( m_blurPingPong[0].get(), 0, nullptr,
                                     pixels, width * 4, 0 );

    // ── Run 4 blur passes: H→V→H→V ────────────────────────────────
    // Pass N reads from ping-pong[N%2], writes to ping-pong[(N+1)%2].
    m_gpuContext->CSSetShader( m_blurCS.get(), nullptr, 0 );

    constexpr int kGroupSize = 256;

    for( int pass = 0; pass < 4; pass++ )
    {
        int src = pass & 1;
        int dst = 1 - src;
        UINT direction = ( pass & 1 ) ? 1u : 0u; // 0=H, 1=V, 0=H, 1=V

        // Update constant buffer.
        D3D11_MAPPED_SUBRESOURCE mapped = {};
        hr = m_gpuContext->Map( m_blurCB.get(), 0, D3D11_MAP_WRITE_DISCARD, 0, &mapped );
        if( FAILED( hr ) ) return false;
        {
            auto* cb = static_cast<GpuBlurConstants*>( mapped.pData );
            cb->Direction = direction;
            cb->Radius = radius;
            cb->Width = width;
            cb->Height = height;
        }
        m_gpuContext->Unmap( m_blurCB.get(), 0 );

        ID3D11Buffer* cbs[] = { m_blurCB.get() };
        m_gpuContext->CSSetConstantBuffers( 0, 1, cbs );

        ID3D11ShaderResourceView* srvs[] = { m_blurPingSRV[src].get() };
        m_gpuContext->CSSetShaderResources( 0, 1, srvs );

        ID3D11UnorderedAccessView* uavs[] = { m_blurPingUAV[dst].get() };
        m_gpuContext->CSSetUnorderedAccessViews( 0, 1, uavs, nullptr );

        // Dispatch: GroupsX covers the blur axis, GroupsY covers the perpendicular axis.
        UINT axisLen = ( direction == 0 ) ? width : height;
        UINT perpLen = ( direction == 0 ) ? height : width;
        UINT groupsX = ( axisLen + kGroupSize - 1 ) / kGroupSize;
        UINT groupsY = perpLen;
        m_gpuContext->Dispatch( groupsX, groupsY, 1 );

        // Unbind to avoid SRV/UAV hazards.
        ID3D11ShaderResourceView* nullSRV[] = { nullptr };
        m_gpuContext->CSSetShaderResources( 0, 1, nullSRV );
        ID3D11UnorderedAccessView* nullUAV[] = { nullptr };
        m_gpuContext->CSSetUnorderedAccessViews( 0, 1, nullUAV, nullptr );
    }

    // Unbind compute shader.
    m_gpuContext->CSSetShader( nullptr, nullptr, 0 );

    // Check for GPU device loss.
    hr = m_gpuDevice->GetDeviceRemovedReason();
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU device removed after blur: 0x%08X\n", hr );
        m_gpuBlurReady = false;
        return false;
    }

    // After 4 passes (H→V→H→V), result is in ping-pong[0] (since final
    // dst = 1 - (3 & 1) = 1 - 1 = 0).
    return true;
}

//----------------------------------------------------------------------------
// WebcamCapture::GpuComposite
//
// Renders the dual-source composite on the GPU and reads back the result
// into m_upscalePixels.  Returns true on success.
//----------------------------------------------------------------------------
bool WebcamCapture::GpuComposite(
    const UINT32* cameraPixels, UINT camW, UINT camH,
    const UINT32* blurPixels, UINT blurW, UINT blurH,
    const float* mask, UINT maskW, UINT maskH,
    UINT outW, UINT outH,
    UINT srcCropX, UINT srcCropY, UINT srcCropW, UINT srcCropH,
    float gamma, Shape shape, float cornerRadius,
    ID3D11ShaderResourceView* preBlurSRV )
{
    HRESULT hr;

    // ── Create / resize camera texture + SRV ───────────────────────
    if( m_gpuCameraW != camW || m_gpuCameraH != camH )
    {
        m_gpuCameraSRV = nullptr;
        m_gpuCameraTex = nullptr;

        D3D11_TEXTURE2D_DESC td = {};
        td.Width = camW;
        td.Height = camH;
        td.MipLevels = 1;
        td.ArraySize = 1;
        td.Format = DXGI_FORMAT_B8G8R8A8_UNORM;
        td.SampleDesc.Count = 1;
        td.Usage = D3D11_USAGE_DEFAULT;
        td.BindFlags = D3D11_BIND_SHADER_RESOURCE;
        hr = m_gpuDevice->CreateTexture2D( &td, nullptr, m_gpuCameraTex.put() );
        if( FAILED( hr ) ) return false;

        hr = m_gpuDevice->CreateShaderResourceView( m_gpuCameraTex.get(), nullptr, m_gpuCameraSRV.put() );
        if( FAILED( hr ) ) return false;

        m_gpuCameraW = camW;
        m_gpuCameraH = camH;
    }

    // ── Create / resize blur texture + SRV ─────────────────────────
    if( m_gpuBlurW != blurW || m_gpuBlurH != blurH )
    {
        m_gpuBlurSRV = nullptr;
        m_gpuBlurTex = nullptr;

        D3D11_TEXTURE2D_DESC td = {};
        td.Width = blurW;
        td.Height = blurH;
        td.MipLevels = 1;
        td.ArraySize = 1;
        td.Format = DXGI_FORMAT_B8G8R8A8_UNORM;
        td.SampleDesc.Count = 1;
        td.Usage = D3D11_USAGE_DEFAULT;
        td.BindFlags = D3D11_BIND_SHADER_RESOURCE;
        hr = m_gpuDevice->CreateTexture2D( &td, nullptr, m_gpuBlurTex.put() );
        if( FAILED( hr ) ) return false;

        hr = m_gpuDevice->CreateShaderResourceView( m_gpuBlurTex.get(), nullptr, m_gpuBlurSRV.put() );
        if( FAILED( hr ) ) return false;

        m_gpuBlurW = blurW;
        m_gpuBlurH = blurH;
    }

    // ── Create / resize mask texture + SRV ─────────────────────────
    if( mask && ( m_gpuMaskW != maskW || m_gpuMaskH != maskH ) )
    {
        m_gpuMaskSRV = nullptr;
        m_gpuMaskTex = nullptr;

        D3D11_TEXTURE2D_DESC td = {};
        td.Width = maskW;
        td.Height = maskH;
        td.MipLevels = 1;
        td.ArraySize = 1;
        td.Format = DXGI_FORMAT_R32_FLOAT;
        td.SampleDesc.Count = 1;
        td.Usage = D3D11_USAGE_DEFAULT;
        td.BindFlags = D3D11_BIND_SHADER_RESOURCE;
        hr = m_gpuDevice->CreateTexture2D( &td, nullptr, m_gpuMaskTex.put() );
        if( FAILED( hr ) ) return false;

        hr = m_gpuDevice->CreateShaderResourceView( m_gpuMaskTex.get(), nullptr, m_gpuMaskSRV.put() );
        if( FAILED( hr ) ) return false;

        m_gpuMaskW = maskW;
        m_gpuMaskH = maskH;
    }

    // ── Create / resize render target + staging ────────────────────
    if( m_gpuRTW != outW || m_gpuRTH != outH )
    {
        m_gpuRTV = nullptr;
        m_gpuRenderTarget = nullptr;
        m_gpuStaging = nullptr;

        D3D11_TEXTURE2D_DESC td = {};
        td.Width = outW;
        td.Height = outH;
        td.MipLevels = 1;
        td.ArraySize = 1;
        td.Format = DXGI_FORMAT_B8G8R8A8_UNORM;
        td.SampleDesc.Count = 1;
        td.Usage = D3D11_USAGE_DEFAULT;
        td.BindFlags = D3D11_BIND_RENDER_TARGET;
        hr = m_gpuDevice->CreateTexture2D( &td, nullptr, m_gpuRenderTarget.put() );
        if( FAILED( hr ) ) return false;

        hr = m_gpuDevice->CreateRenderTargetView( m_gpuRenderTarget.get(), nullptr, m_gpuRTV.put() );
        if( FAILED( hr ) ) return false;

        // Staging for GPU → CPU readback.
        td.Usage = D3D11_USAGE_STAGING;
        td.BindFlags = 0;
        td.CPUAccessFlags = D3D11_CPU_ACCESS_READ;
        hr = m_gpuDevice->CreateTexture2D( &td, nullptr, m_gpuStaging.put() );
        if( FAILED( hr ) ) return false;

        m_gpuRTW = outW;
        m_gpuRTH = outH;
    }

    // ── Upload pixel data ──────────────────────────────────────────
    m_gpuContext->UpdateSubresource( m_gpuCameraTex.get(), 0, nullptr,
                                     cameraPixels, camW * 4, 0 );

    // When a pre-blurred SRV is provided (from GpuBoxBlur), skip the
    // CPU blur upload — the data is already GPU-resident.
    if( !preBlurSRV )
    {
        m_gpuContext->UpdateSubresource( m_gpuBlurTex.get(), 0, nullptr,
                                         blurPixels, blurW * 4, 0 );
    }
    if( mask && m_gpuMaskTex )
        m_gpuContext->UpdateSubresource( m_gpuMaskTex.get(), 0, nullptr,
                                         mask, maskW * sizeof(float), 0 );

    // ── Update constant buffer ─────────────────────────────────────
    D3D11_MAPPED_SUBRESOURCE mapped = {};
    hr = m_gpuContext->Map( m_compositeCB.get(), 0, D3D11_MAP_WRITE_DISCARD, 0, &mapped );
    if( FAILED( hr ) ) return false;
    {
        auto* cb = static_cast<GpuCompositeConstants*>( mapped.pData );
        cb->CropOffsetX = static_cast<float>( srcCropX ) / camW;
        cb->CropOffsetY = static_cast<float>( srcCropY ) / camH;
        cb->CropScaleX  = static_cast<float>( srcCropW ) / camW;
        cb->CropScaleY  = static_cast<float>( srcCropH ) / camH;
        cb->Gamma       = gamma;
        cb->CornerRadius = cornerRadius;
        cb->OutputW     = static_cast<float>( outW );
        cb->OutputH     = static_cast<float>( outH );
        cb->ShapeType   = static_cast<UINT>( shape );
        cb->HasMask     = ( mask != nullptr ) ? 1u : 0u;
        cb->Pad[0]      = 0;
        cb->Pad[1]      = 0;
    }
    m_gpuContext->Unmap( m_compositeCB.get(), 0 );

    // ── Set pipeline state ─────────────────────────────────────────
    // Clear render target to black to avoid stale GPU memory.
    const float clearColor[4] = { 0, 0, 0, 0 };
    m_gpuContext->ClearRenderTargetView( m_gpuRTV.get(), clearColor );

    m_gpuContext->IASetPrimitiveTopology( D3D11_PRIMITIVE_TOPOLOGY_TRIANGLELIST );
    m_gpuContext->IASetInputLayout( nullptr );  // No vertex buffer.

    m_gpuContext->VSSetShader( m_compositeVS.get(), nullptr, 0 );
    m_gpuContext->PSSetShader( m_compositePS.get(), nullptr, 0 );

    m_gpuContext->RSSetState( m_gpuRasterState.get() );
    m_gpuContext->OMSetBlendState( m_gpuBlendState.get(), nullptr, 0xFFFFFFFF );

    ID3D11Buffer* cbs[] = { m_compositeCB.get() };
    m_gpuContext->PSSetConstantBuffers( 0, 1, cbs );

    ID3D11ShaderResourceView* srvs[] = {
        m_gpuCameraSRV.get(),
        preBlurSRV ? preBlurSRV : m_gpuBlurSRV.get(),
        mask ? m_gpuMaskSRV.get() : nullptr
    };
    m_gpuContext->PSSetShaderResources( 0, 3, srvs );

    ID3D11SamplerState* samplers[] = { m_bilinearSampler.get() };
    m_gpuContext->PSSetSamplers( 0, 1, samplers );

    ID3D11RenderTargetView* rtvs[] = { m_gpuRTV.get() };
    m_gpuContext->OMSetRenderTargets( 1, rtvs, nullptr );

    D3D11_VIEWPORT vp = {};
    vp.Width  = static_cast<float>( outW );
    vp.Height = static_cast<float>( outH );
    vp.MaxDepth = 1.0f;
    m_gpuContext->RSSetViewports( 1, &vp );

    // ── Draw full-screen triangle ──────────────────────────────────
    m_gpuContext->Draw( 3, 0 );

    // Check for GPU device loss (driver crash, TDR, etc.).
    hr = m_gpuDevice->GetDeviceRemovedReason();
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] GPU device removed after Draw: 0x%08X\n", hr );
        m_gpuCompositeReady = false;
        return false;
    }

    // Unbind SRVs to avoid hazard warnings.
    ID3D11ShaderResourceView* nullSRVs[3] = {};
    m_gpuContext->PSSetShaderResources( 0, 3, nullSRVs );

    // ── Read back result ───────────────────────────────────────────
    m_gpuContext->CopyResource( m_gpuStaging.get(), m_gpuRenderTarget.get() );

    D3D11_MAPPED_SUBRESOURCE readback = {};
    hr = m_gpuContext->Map( m_gpuStaging.get(), 0, D3D11_MAP_READ, 0, &readback );
    if( FAILED( hr ) ) return false;

    const size_t outSize = static_cast<size_t>( outW ) * outH * 4;
    m_upscalePixels.resize( outSize );
    const UINT outRowBytes = outW * 4;
    if( readback.RowPitch < outRowBytes )
    {
        OutputDebug( L"[WebcamCapture] GPU readback RowPitch %u < expected %u\n",
                     readback.RowPitch, outRowBytes );
        m_gpuContext->Unmap( m_gpuStaging.get(), 0 );
        return false;
    }
    for( UINT row = 0; row < outH; row++ )
    {
        memcpy( m_upscalePixels.data() + row * outRowBytes,
                static_cast<const BYTE*>( readback.pData ) + row * readback.RowPitch,
                outRowBytes );
    }
    m_gpuContext->Unmap( m_gpuStaging.get(), 0 );

    return true;
}

//----------------------------------------------------------------------------
// WebcamCapture::InitSourceReader
//
// Opens the camera via IMFSourceReader.  Negotiates BGRA output so frames
// can be composited directly.  Uses the D3D11 device manager for
// GPU-accelerated decoding when the driver supports it.
//----------------------------------------------------------------------------
bool WebcamCapture::InitSourceReader()
{
    HRESULT hr = MFStartup( MF_VERSION );
    if( FAILED( hr ) )
    {
        OutputDebug( L"[WebcamCapture] MFStartup failed\n" );
        return false;
    }
    m_mfStarted = true;

    // Create a media source for the camera.
    winrt::com_ptr<IMFAttributes> sourceAttrs;
    hr = MFCreateAttributes( sourceAttrs.put(), 1 );
    if( FAILED( hr ) ) { OutputDebug( L"[WebcamCapture] MFCreateAttributes failed\n" ); return false; }

    sourceAttrs->SetGUID( MF_DEVSOURCE_ATTRIBUTE_SOURCE_TYPE,
                          MF_DEVSOURCE_ATTRIBUTE_SOURCE_TYPE_VIDCAP_GUID );

    // Enumerate video capture devices.
    IMFActivate** ppDevices = nullptr;
    UINT32 count = 0;
    hr = MFEnumDeviceSources( sourceAttrs.get(), &ppDevices, &count );
    if( FAILED( hr ) || count == 0 )
    {
        if( ppDevices ) CoTaskMemFree( ppDevices );
        OutputDebug( L"[WebcamCapture] No cameras found\n" );
        return false;
    }

    // Find matching device by symlink, or use the first one.
    UINT32 deviceIndex = 0;
    if( !m_deviceSymLink.empty() )
    {
        for( UINT32 i = 0; i < count; i++ )
        {
            WCHAR* symLink = nullptr;
            UINT32 symLinkLen = 0;
            if( SUCCEEDED( ppDevices[i]->GetAllocatedString(
                    MF_DEVSOURCE_ATTRIBUTE_SOURCE_TYPE_VIDCAP_SYMBOLIC_LINK,
                    &symLink, &symLinkLen ) ) )
            {
                if( _wcsicmp( symLink, m_deviceSymLink.c_str() ) == 0 )
                    deviceIndex = i;
                CoTaskMemFree( symLink );
            }
        }
    }

    winrt::com_ptr<IMFMediaSource> mediaSource;
    hr = ppDevices[deviceIndex]->ActivateObject( IID_PPV_ARGS( mediaSource.put() ) );
    for( UINT32 i = 0; i < count; i++ )
        ppDevices[i]->Release();
    CoTaskMemFree( ppDevices );
    if( FAILED( hr ) ) { OutputDebug( L"[WebcamCapture] ActivateObject failed\n" ); return false; }

    // Ensure camera auto-brightness, auto-exposure, and backlight
    // compensation are enabled.  Without this, many cameras start with
    // conservative settings that produce noticeably darker output compared
    // to apps like Teams or the Windows Camera app that configure these
    // properties explicitly.
    {
        winrt::com_ptr<IAMVideoProcAmp> videoProcAmp;
        if( SUCCEEDED( mediaSource->QueryInterface( IID_PPV_ARGS( videoProcAmp.put() ) ) ) )
        {
            long minVal, maxVal, step, defaultVal, flags;

            // Auto brightness.
            if( SUCCEEDED( videoProcAmp->GetRange( VideoProcAmp_Brightness,
                    &minVal, &maxVal, &step, &defaultVal, &flags ) ) )
            {
                OutputDebug( L"[WebcamCapture] Brightness range: %d-%d step=%d default=%d flags=0x%X\n",
                             minVal, maxVal, step, defaultVal, flags );
                videoProcAmp->Set( VideoProcAmp_Brightness, defaultVal,
                                   VideoProcAmp_Flags_Auto );
            }

            // Backlight compensation — critical for scenes with a bright
            // background (e.g. window behind the person).  The camera
            // boosts face exposure instead of underexposing for highlights.
            if( SUCCEEDED( videoProcAmp->GetRange( VideoProcAmp_BacklightCompensation,
                    &minVal, &maxVal, &step, &defaultVal, &flags ) ) )
            {
                OutputDebug( L"[WebcamCapture] BacklightComp range: %d-%d default=%d flags=0x%X\n",
                             minVal, maxVal, defaultVal, flags );
                videoProcAmp->Set( VideoProcAmp_BacklightCompensation,
                                   maxVal, VideoProcAmp_Flags_Manual );
            }

            // Auto white balance.
            if( SUCCEEDED( videoProcAmp->GetRange( VideoProcAmp_WhiteBalance,
                    &minVal, &maxVal, &step, &defaultVal, &flags ) ) )
            {
                videoProcAmp->Set( VideoProcAmp_WhiteBalance, defaultVal,
                                   VideoProcAmp_Flags_Auto );
            }

            // Auto contrast.
            if( SUCCEEDED( videoProcAmp->GetRange( VideoProcAmp_Contrast,
                    &minVal, &maxVal, &step, &defaultVal, &flags ) ) )
            {
                videoProcAmp->Set( VideoProcAmp_Contrast, defaultVal,
                                   VideoProcAmp_Flags_Auto );
            }

            // Auto gain.
            if( SUCCEEDED( videoProcAmp->GetRange( VideoProcAmp_Gain,
                    &minVal, &maxVal, &step, &defaultVal, &flags ) ) )
            {
                videoProcAmp->Set( VideoProcAmp_Gain, defaultVal,
                                   VideoProcAmp_Flags_Auto );
            }
        }
        else
        {
            OutputDebug( L"[WebcamCapture] IAMVideoProcAmp not supported\n" );
        }

        winrt::com_ptr<IAMCameraControl> cameraControl;
        if( SUCCEEDED( mediaSource->QueryInterface( IID_PPV_ARGS( cameraControl.put() ) ) ) )
        {
            long minVal, maxVal, step, defaultVal, flags;
            if( SUCCEEDED( cameraControl->GetRange( CameraControl_Exposure,
                    &minVal, &maxVal, &step, &defaultVal, &flags ) ) )
            {
                OutputDebug( L"[WebcamCapture] Exposure range: %d-%d step=%d default=%d flags=0x%X\n",
                             minVal, maxVal, step, defaultVal, flags );
                // Enable auto-exposure.  Use max (brightest) as the
                // starting point rather than the default, which on many
                // cameras is the minimum (darkest) value.
                cameraControl->Set( CameraControl_Exposure, maxVal,
                                    CameraControl_Flags_Auto );
            }
        }
    }

    // Pin the media source to the highest supported resolution so the
    // source reader's video processor only needs to convert color space
    // (NV12 → RGB32) without resizing.  Without this, the MF video
    // processor may silently deliver a lower resolution even when
    // SetCurrentMediaType(1920×1080 RGB32) succeeds.
    {
        winrt::com_ptr<IMFPresentationDescriptor> pd;
        if( SUCCEEDED( mediaSource->CreatePresentationDescriptor( pd.put() ) ) )
        {
            BOOL selected = FALSE;
            winrt::com_ptr<IMFStreamDescriptor> sd;
            if( SUCCEEDED( pd->GetStreamDescriptorByIndex( 0, &selected, sd.put() ) ) )
            {
                winrt::com_ptr<IMFMediaTypeHandler> handler;
                if( SUCCEEDED( sd->GetMediaTypeHandler( handler.put() ) ) )
                {
                    DWORD typeCount = 0;
                    handler->GetMediaTypeCount( &typeCount );

                    // Preferred resolutions (highest first).
                    const struct { UINT w, h; } preferred[] = {
                        { 1920, 1080 }, { 1280, 720 },
                    };
                    bool pinned = false;
                    for( const auto& pref : preferred )
                    {
                        for( DWORD i = 0; i < typeCount && !pinned; i++ )
                        {
                            winrt::com_ptr<IMFMediaType> mt;
                            if( SUCCEEDED( handler->GetMediaTypeByIndex( i, mt.put() ) ) )
                            {
                                UINT32 w = 0, h = 0;
                                MFGetAttributeSize( mt.get(), MF_MT_FRAME_SIZE, &w, &h );
                                if( w == pref.w && h == pref.h )
                                {
                                    if( SUCCEEDED( handler->SetCurrentMediaType( mt.get() ) ) )
                                    {
                                        OutputDebug( L"[WebcamCapture] Pinned native type to %ux%u\n", w, h );
                                        pinned = true;
                                    }
                                }
                            }
                        }
                        if( pinned ) break;
                    }
                }
            }
        }
    }

    // Create source reader attributes (request BGRA output).
    winrt::com_ptr<IMFAttributes> readerAttrs;
    hr = MFCreateAttributes( readerAttrs.put(), 3 );
    if( FAILED( hr ) ) return false;

    // Enable the software video processor so the source reader converts
    // NV12/YUY2/MJPEG → BGRA.  This is needed regardless of whether we
    // have a DXGI device manager — it tells the source reader to perform
    // format conversion.
    readerAttrs->SetUINT32( MF_SOURCE_READER_ENABLE_VIDEO_PROCESSING, TRUE );

    hr = MFCreateSourceReaderFromMediaSource( mediaSource.get(), readerAttrs.get(),
                                              m_sourceReader.put() );
    if( FAILED( hr ) ) { OutputDebug( L"[WebcamCapture] MFCreateSourceReaderFromMediaSource failed\n" ); return false; }

    // Set the output media type.  Try RGB32 (BGRX, no alpha) first — this
    // is the format the MF video processor most reliably converts to from
    // NV12/YUY2/MJPEG.  ARGB32 (BGRA with alpha) is not supported by many
    // cameras' video processor chains.
    //
    // Request native-size first (no explicit frame size).  The MF software
    // video processor converts color space reliably but does NOT always
    // resize: on some cameras SetCurrentMediaType(640x480) succeeds yet
    // ReadSample still delivers buffers at the camera's native resolution
    // (e.g. 1920x1080).  Since CaptureThread pre-scales every frame to
    // overlay dimensions anyway, native-size avoids the mismatch and is
    // equally fast.
    const GUID formatsToTry[] = { MFVideoFormat_RGB32, MFVideoFormat_ARGB32 };
    bool formatSet = false;

    // Prefer native size — the media source was already pinned to the
    // highest resolution above, and skipping the MF resize avoids the
    // bug where SetCurrentMediaType(1920×1080) succeeds but ReadSample
    // delivers a smaller buffer.
    struct { UINT w, h; } resToTry[] = {
        {    0,    0 },  // native / unspecified — preferred
        { 1920, 1080 },
        { 1280,  720 },
        {  640,  480 },  // last-resort fallback
    };

    for( const auto& subtype : formatsToTry )
    {
        for( const auto& res : resToTry )
        {
            winrt::com_ptr<IMFMediaType> outputType;
            hr = MFCreateMediaType( outputType.put() );
            if( FAILED( hr ) ) continue;
            outputType->SetGUID( MF_MT_MAJOR_TYPE, MFMediaType_Video );
            outputType->SetGUID( MF_MT_SUBTYPE, subtype );

            // Explicitly request full-range (0-255) RGB output.  Without this,
            // the MF software video processor may assume limited-range (16-235)
            // input when converting from YUV, which compresses the luminance
            // range and produces a darker image than apps (e.g. Teams) that use
            // the hardware video processor or set these attributes explicitly.
            outputType->SetUINT32( MF_MT_VIDEO_NOMINAL_RANGE, MFNominalRange_0_255 );

            if( res.w != 0 )
                MFSetAttributeSize( outputType.get(), MF_MT_FRAME_SIZE, res.w, res.h );

            hr = m_sourceReader->SetCurrentMediaType(
                static_cast<DWORD>(MF_SOURCE_READER_FIRST_VIDEO_STREAM),
                nullptr,
                outputType.get() );
            if( SUCCEEDED( hr ) )
            {
                if( res.w != 0 )
                    OutputDebug( L"[WebcamCapture] Negotiated output: %ux%u\n", res.w, res.h );
                else
                    OutputDebug( L"[WebcamCapture] Negotiated output: native size\n" );
                formatSet = true;
                break;
            }
        }
        if( formatSet ) break;
    }

    if( !formatSet )
    {
        OutputDebug( L"[WebcamCapture] SetCurrentMediaType failed for all formats\n" );
        return false;
    }

    // Read the actual negotiated frame size.
    winrt::com_ptr<IMFMediaType> actualType;
    hr = m_sourceReader->GetCurrentMediaType(
        static_cast<DWORD>(MF_SOURCE_READER_FIRST_VIDEO_STREAM),
        actualType.put() );
    if( SUCCEEDED( hr ) )
    {
        MFGetAttributeSize( actualType.get(), MF_MT_FRAME_SIZE, &m_camWidth, &m_camHeight );
    }

    if( m_camWidth == 0 || m_camHeight == 0 )
    {
        m_camWidth = 640;
        m_camHeight = 480;
    }

    OutputDebug( L"[WebcamCapture] Camera opened: %ux%u\n", m_camWidth, m_camHeight );

    return true;
}

//----------------------------------------------------------------------------
// WebcamCapture::Start
//----------------------------------------------------------------------------
bool WebcamCapture::Start()
{
    m_running.store( true );
    m_thread = std::thread( &WebcamCapture::CaptureThread, this );

    // Don't block — let the capture thread warm up the camera asynchronously.
    // CompositeOnto will return false until the first frame arrives, so the
    // first few video frames won't have the webcam overlay, but recording
    // starts immediately instead of stalling ~1 s for camera warmup.
    return true;
}

//----------------------------------------------------------------------------
// WebcamCapture::WaitForFirstFrame
//----------------------------------------------------------------------------
bool WebcamCapture::WaitForFirstFrame( int timeoutMs )
{
    std::unique_lock<std::mutex> lock( m_readyMutex );
    m_readyCV.wait_for( lock, std::chrono::milliseconds( timeoutMs ),
                        [this]{ return m_firstFrameCaptured || m_initFailed.load( std::memory_order_acquire ); } );
    return m_firstFrameCaptured;
}

//----------------------------------------------------------------------------
// WebcamCapture::Stop
//----------------------------------------------------------------------------
void WebcamCapture::Stop()
{
    m_running.store( false );
    if( m_thread.joinable() )
        m_thread.join();

    {
        std::lock_guard<std::mutex> lock( m_frameLock );
        m_pendingPixels.clear();
        m_pendingW = 0;
        m_pendingH = 0;
        m_newFrameReady = false;
    }
    m_overlayTex = nullptr;
    m_hasOverlay = false;
    m_texW = 0;
    m_texH = 0;
}

//----------------------------------------------------------------------------
// WebcamCapture::CaptureThread
//
// Reads frames from the source reader.  Each frame is converted to a
// D3D11 texture and stored as the latest frame for compositing.
//----------------------------------------------------------------------------
void WebcamCapture::CaptureThread()
{
    // ALL Media Foundation work must happen on the same thread:
    // COM init, MFStartup, source reader creation, and ReadSample.
    // IMFSourceReader is not thread-safe across different apartment threads.
    CoInitializeEx( nullptr, COINIT_MULTITHREADED );

    if( !InitSourceReader() )
    {
        OutputDebug( L"[WebcamCapture] InitSourceReader failed on capture thread\n" );
        m_initFailed.store( true, std::memory_order_release );
        // Wake up WaitForFirstFrame so it doesn't block for the full timeout.
        {
            std::lock_guard<std::mutex> lock( m_readyMutex );
            m_firstFrameCaptured = false;
        }
        m_readyCV.notify_all();
        CoUninitialize();
        return;
    }

    OutputDebug( L"[WebcamCapture] Capture thread started, reading frames...\n" );

    // Initialize background processing if enabled.
    if( m_backgroundMode != WebcamBackgroundMode::None )
    {
        m_backgroundBlur = std::make_unique<BackgroundBlur>();

        // Extract the segmentation model from embedded resources to %TEMP%.
        WCHAR tempDir[MAX_PATH] = {};
        GetTempPath( MAX_PATH, tempDir );
        std::wstring modelPath = std::wstring( tempDir ) + L"selfie_segmentation.onnx";

        HRSRC hRes = FindResource( NULL, L"RCSEGMODEL", L"BINRES" );
        if( hRes )
        {
            HGLOBAL hData = LoadResource( NULL, hRes );
            DWORD   size  = SizeofResource( NULL, hRes );
            LPVOID  pData = hData ? LockResource( hData ) : nullptr;
            if( pData && size > 0 )
            {
                HANDLE hFile = CreateFile( modelPath.c_str(), GENERIC_WRITE, 0, NULL,
                                           CREATE_ALWAYS, FILE_ATTRIBUTE_NORMAL, NULL );
                if( hFile != INVALID_HANDLE_VALUE )
                {
                    DWORD written;
                    WriteFile( hFile, pData, size, &written, NULL );
                    CloseHandle( hFile );
                }
            }
        }

        if( m_backgroundBlur->Initialize( modelPath.c_str() ) )
        {
            OutputDebug( L"[WebcamCapture] Background blur initialized\n" );

            // If image mode, load the background image.
            if( m_backgroundMode == WebcamBackgroundMode::Image && !m_backgroundImagePath.empty() )
            {
                if( m_backgroundBlur->SetBackgroundImage( m_backgroundImagePath.c_str() ) )
                {
                    OutputDebug( L"[WebcamCapture] Background image loaded: %s\n", m_backgroundImagePath.c_str() );
                }
                else
                {
                    OutputDebug( L"[WebcamCapture] Background image failed to load, falling back to blur\n" );
                    m_backgroundMode = WebcamBackgroundMode::Blur;
                }
            }
        }
        else
        {
            OutputDebug( L"[WebcamCapture] Background blur model failed to load: %s\n", modelPath.c_str() );
            m_backgroundBlur.reset();
        }
    }

    // Initialize GPU composite pipeline for hardware-accelerated
    // compositing.  Falls back to CPU if GPU is not available.
    m_gpuCompositeReady = InitGpuComposite();

    int frameCount = 0;
#if _DEBUG
    LARGE_INTEGER perfFreq = {}, lastFrameTime = {}, loopStart = {};
    QueryPerformanceFrequency( &perfFreq );
    QueryPerformanceCounter( &lastFrameTime );
    double totalReadMs = 0, totalCopyMs = 0, totalScaleMs = 0, totalLockMs = 0;
#endif

    while( m_running.load() )
    {
        DWORD streamIndex = 0, flags = 0;
        LONGLONG timestamp = 0;
        winrt::com_ptr<IMFSample> sample;

#if _DEBUG
        QueryPerformanceCounter( &loopStart );
#endif

        HRESULT hr = m_sourceReader->ReadSample(
            static_cast<DWORD>(MF_SOURCE_READER_FIRST_VIDEO_STREAM),
            0,
            &streamIndex,
            &flags,
            &timestamp,
            sample.put() );

        if( FAILED( hr ) || ( flags & MF_SOURCE_READERF_ENDOFSTREAM ) )
        {
            OutputDebug( L"[WebcamCapture] ReadSample exit: hr=0x%08X flags=0x%X frames=%d\n",
                         static_cast<unsigned>( hr ), flags, frameCount );
            break;
        }

        if( !sample )
            continue;

#if _DEBUG
        LARGE_INTEGER afterRead;
        QueryPerformanceCounter( &afterRead );
        double readMs = static_cast<double>( afterRead.QuadPart - loopStart.QuadPart ) * 1000.0 / perfFreq.QuadPart;
        totalReadMs += readMs;
#endif

        // Get the buffer and flatten it into a known-tight BGRA buffer.
        // Use IMF2DBuffer + MFCopyImage when available so the true source
        // stride is honored. Some camera/MF pipelines expose padded or
        // otherwise non-trivial row pitch, and assuming tight packing
        // produces the striped corruption seen in the overlay.
        winrt::com_ptr<IMFMediaBuffer> buffer;
        DWORD bufferCount = 0;
        hr = sample->GetBufferCount( &bufferCount );
        if( FAILED( hr ) || bufferCount == 0 )
            continue;

        if( bufferCount == 1 )
        {
            hr = sample->GetBufferByIndex( 0, buffer.put() );
        }
        else
        {
            hr = sample->ConvertToContiguousBuffer( buffer.put() );
        }
        if( FAILED( hr ) || !buffer )
        {
            OutputDebug( L"[WebcamCapture] Failed to get sample buffer\n" );
            continue;
        }

        UINT rowBytes = m_camWidth * 4;
        DWORD frameBytes = static_cast<DWORD>( rowBytes * m_camHeight );
        m_framePixels.resize( frameBytes );

        bool copiedFrame = false;
        if( auto buffer2D = buffer.try_as<IMF2DBuffer>() )
        {
            BYTE* srcData = nullptr;
            LONG srcStride = 0;
            hr = buffer2D->Lock2D( &srcData, &srcStride );
            if( SUCCEEDED( hr ) && srcData != nullptr )
            {
                const LONG absStride = abs( srcStride );

                // Detect stride mismatch: the MF video processor may
                // convert color space without resizing, so the actual
                // frame can be smaller than the negotiated output.
                if( absStride > 0 &&
                    absStride != static_cast<LONG>( rowBytes ) )
                {
                    DWORD contLen = 0;
                    buffer2D->GetContiguousLength( &contLen );
                    UINT actualW = static_cast<UINT>( absStride ) / 4;
                    UINT actualH = ( contLen > 0 )
                                 ? contLen / static_cast<UINT>( absStride )
                                 : m_camHeight;

                    if( actualW >= 160 && actualW <= 7680 &&
                        actualH >= 120 && actualH <= 4320 )
                    {
                        OutputDebug( L"[WebcamCapture] Lock2D stride mismatch: "
                                     L"negotiated %ux%u (stride=%u) but Lock2D "
                                     L"stride=%d contLen=%u → actual %ux%u\n",
                                     m_camWidth, m_camHeight, rowBytes,
                                     srcStride, contLen, actualW, actualH );
                        m_camWidth  = actualW;
                        m_camHeight = actualH;
                        rowBytes    = actualW * 4;
                        frameBytes  = rowBytes * actualH;
                        m_framePixels.resize( frameBytes );
                    }
                }

                hr = MFCopyImage( m_framePixels.data(), rowBytes,
                                  srcData, srcStride,
                                  rowBytes, m_camHeight );
                buffer2D->Unlock2D();
                copiedFrame = SUCCEEDED( hr );
            }
        }

        if( !copiedFrame )
        {
            BYTE* data = nullptr;
            DWORD maxLen = 0, curLen = 0;
            hr = buffer->Lock( &data, &maxLen, &curLen );
            if( FAILED( hr ) || !data )
                continue;

            // Safety net: if the buffer size doesn't match the negotiated
            // frame, the video processor delivered a different resolution.
            // Re-derive the dimensions from the buffer so we copy the
            // correct pixels.
            if( curLen != frameBytes && curLen > 0 )
            {
                // Check common heights to find one that divides evenly
                // into 4-byte-per-pixel rows.
                const UINT heights[] = { 1080, 720, 480, 960, 1440, 2160 };
                for( UINT h : heights )
                {
                    if( curLen % ( h * 4 ) == 0 )
                    {
                        UINT w = curLen / ( h * 4 );
                        if( w >= 160 && w <= 7680 )
                        {
                            OutputDebug( L"[WebcamCapture] Buffer mismatch: "
                                        L"negotiated %ux%u (%u bytes) but buffer is "
                                        L"%u bytes — using %ux%u\n",
                                        m_camWidth, m_camHeight, frameBytes,
                                        curLen, w, h );
                            m_camWidth = w;
                            m_camHeight = h;
                            break;
                        }
                    }
                }

                const UINT newRowBytes = m_camWidth * 4;
                const DWORD newFrameBytes = static_cast<DWORD>( newRowBytes * m_camHeight );
                m_framePixels.resize( newFrameBytes );
                memcpy_s( m_framePixels.data(), newFrameBytes, data, min( curLen, newFrameBytes ) );
            }
            else
            {
                memcpy_s( m_framePixels.data(), frameBytes, data, min( curLen, frameBytes ) );
            }
            buffer->Unlock();
            copiedFrame = true;
        }

        if( !copiedFrame )
            continue;

#if _DEBUG
        LARGE_INTEGER afterCopy;
        QueryPerformanceCounter( &afterCopy );
        double copyMs = static_cast<double>( afterCopy.QuadPart - afterRead.QuadPart ) * 1000.0 / perfFreq.QuadPart;
        totalCopyMs += copyMs;
#endif

        // Compute overlay dimensions if not yet done.
        if( m_overlayW == 0 )
            ComputeOverlayDimensions();

        const UINT ovW = m_overlayW;
        const UINT ovH = m_overlayH;
        if( ovW > 0 && ovH > 0 )
        {
            // Cap the CPU processing resolution so blur + mask
            // processing stays fast.  The blur runs on the CPU (4-pass
            // box blur) so this has a major impact on frame rate.  The
            // GPU composite uses the full-res camera for the foreground
            // person, so only the background blur is at reduced res.
            constexpr UINT kMaxProcDim = 960;
            UINT procW = ovW;
            UINT procH = ovH;
            if( procW > kMaxProcDim || procH > kMaxProcDim )
            {
                float scale = static_cast<float>( kMaxProcDim ) /
                              static_cast<float>( max( procW, procH ) );
                procW = max( 1u, static_cast<UINT>( ovW * scale ) );
                procH = max( 1u, static_cast<UINT>( ovH * scale ) );
            }

            // Pre-scale camera frame to processing dimensions (CPU only).
            // NO D3D calls here — the capture thread must not touch the
            // recording session's D3D device to avoid GPU contention.
            const UINT scaledStride = procW * 4;
            const size_t scaledSize = static_cast<size_t>( scaledStride ) * procH;
            m_scaledPixels.resize( scaledSize );

            const UINT32* srcPixels = reinterpret_cast<const UINT32*>( m_framePixels.data() );
            UINT32* dstPixels = reinterpret_cast<UINT32*>( m_scaledPixels.data() );
            const UINT srcW32 = m_camWidth;  // stride in uint32 units

            // For FullScreen mode, crop the camera feed to match the
            // output aspect ratio (crop-to-fill) so nothing is stretched.
            // For other modes the full camera frame is used.
            UINT srcCropX = 0, srcCropY = 0;
            UINT srcCropW = m_camWidth, srcCropH = m_camHeight;

            if( m_camWidth > 0 && m_camHeight > 0 && procW > 0 && procH > 0 &&
                ( (m_size == FullScreen && !m_fullScreenRecording) ||
                  m_shape == Circle || m_shape == RoundedSquare ) )
            {
                // Compare aspect ratios: camera vs output.
                // Scale camera so it fills the output, then crop excess.
                double camAspect = static_cast<double>( m_camWidth ) / m_camHeight;
                double outAspect = static_cast<double>( procW ) / procH;

                if( camAspect > outAspect )
                {
                    // Camera is wider — crop sides.
                    srcCropH = m_camHeight;
                    srcCropW = static_cast<UINT>( m_camHeight * outAspect + 0.5 );
                    srcCropX = ( m_camWidth - srcCropW ) / 2;
                }
                else
                {
                    // Camera is taller — crop top/bottom.
                    srcCropW = m_camWidth;
                    srcCropH = static_cast<UINT>( m_camWidth / outAspect + 0.5 );
                    srcCropY = ( m_camHeight - srcCropH ) / 2;
                }
            }

            // Pre-compute shape mask parameters.
            const float halfW = procW * 0.5f;
            const float halfH = procH * 0.5f;
            // Rounded-rect corner radius: 10% of the smaller dimension.
            // Rounded-square corner radius: 40% for exaggerated rounding.
            const float cornerRadius = min( halfW, halfH ) *
                ( m_shape == RoundedSquare ? 0.40f : 0.10f );

            for( UINT y = 0; y < procH; y++ )
            {
                // Simple nearest-neighbor downsample for blur input.
                // Quality doesn't matter here — the result will be blurred.
                const UINT srcY = srcCropY + y * srcCropH / procH;
                const UINT32* srcRow = srcPixels + static_cast<size_t>( srcY ) * srcW32;
                UINT32* dstRow = dstPixels + static_cast<size_t>( y ) * procW;
                for( UINT x = 0; x < procW; x++ )
                {
                    const UINT srcX = srcCropX + x * srcCropW / procW;
                    UINT32 pixel = srcRow[srcX];
                    UINT32 b = m_gammaLUT[(pixel      ) & 0xFF];
                    UINT32 g = m_gammaLUT[(pixel >>  8) & 0xFF];
                    UINT32 r = m_gammaLUT[(pixel >> 16) & 0xFF];
                    dstRow[x] = 0xFF000000u | (r << 16) | (g << 8) | b;
                }
            }

            // Apply background processing at the capped processing
            // resolution — dramatically faster than full output size.
            //
            // GPU blur is only used when upscaling is needed (overlay >
            // proc buffer), since GpuComposite handles blur+mask+upscale
            // together.  For small overlays where no upscale is needed,
            // the CPU Apply() is fast enough at the capped resolution.
            const bool needUpscale = ( procW < ovW || procH < ovH );
            bool gpuBlurDone = false;
            if( m_backgroundBlur && m_backgroundBlur->IsInitialized() )
            {
                if( m_backgroundMode == WebcamBackgroundMode::Image && m_backgroundBlur->HasBackgroundImage() )
                {
                    m_backgroundBlur->ApplyImageReplacement(
                        reinterpret_cast<uint8_t*>( m_scaledPixels.data() ),
                        procW, procH );
                }
                else if( m_backgroundMode == WebcamBackgroundMode::Blur )
                {
                    // Try GPU blur first when conditions are met.
                    // Run GpuBoxBlur BEFORE segmentation so that if it
                    // fails, no CPU-side state has been modified and the
                    // full CPU Apply() path works cleanly as a fallback.
                    if( m_gpuBlurReady && m_gpuCompositeReady && needUpscale )
                    {
                        gpuBlurDone = GpuBoxBlur( dstPixels, procW, procH, 21 );
                    }

                    if( gpuBlurDone )
                    {
                        // GPU blur succeeded — run segmentation only
                        // (model-res mask; GPU bilinear handles upscale).
                        m_backgroundBlur->RunSegmentationOnly(
                            reinterpret_cast<const uint8_t*>( m_scaledPixels.data() ),
                            procW, procH );
                    }
                    else
                    {
                        // CPU path: full segmentation + blur + mask blend.
                        m_backgroundBlur->Apply(
                            reinterpret_cast<uint8_t*>( m_scaledPixels.data() ),
                            procW, procH, 21 );
                    }
                }
            }

            // ── Composite at overlay size ──────────────────────────
            // When the overlay is larger than the processing buffer,
            // composite sharp foreground (from full-res camera) with
            // blurred background (from 960px proc buffer) using the
            // segmentation mask.
            //
            // GPU path: hardware bilinear + parallel pixel processing.
            // CPU fallback: per-pixel loop with shape masking.
            const bool hasMask = m_backgroundBlur &&
                                 m_backgroundBlur->IsInitialized() &&
                                 m_backgroundBlur->HasCachedMask() &&
                                 m_backgroundMode == WebcamBackgroundMode::Blur;

            UINT finalW = procW, finalH = procH;
            UINT32* finalPixels = dstPixels;

            bool gpuDone = false;
            if( m_gpuCompositeReady && needUpscale )
            {
                const float* maskPtr = nullptr;
                UINT mW = 0, mH = 0;
                const UINT32* blurPtr = dstPixels;  // proc buffer (with blur applied)
                ID3D11ShaderResourceView* preBlurSRV = nullptr;

                if( hasMask )
                {
                    if( gpuBlurDone )
                    {
                        // GPU blur result is already GPU-resident in
                        // m_blurPingSRV[0] — pass it directly to the
                        // composite shader to avoid a CPU→GPU upload.
                        preBlurSRV = m_blurPingSRV[0].get();

                        // Use model-resolution mask (e.g. 256×256) — the
                        // GPU bilinear sampler upscales it for free,
                        // eliminating the CPU upscale+feather work.
                        maskPtr = m_backgroundBlur->GetModelMask().data();
                        mW = static_cast<UINT>( m_backgroundBlur->GetModelMaskWidth() );
                        mH = static_cast<UINT>( m_backgroundBlur->GetModelMaskHeight() );
                    }
                    else
                    {
                        maskPtr = m_backgroundBlur->GetMask().data();
                        mW = m_backgroundBlur->GetMaskWidth();
                        mH = m_backgroundBlur->GetMaskHeight();

                        const auto& blurFrame = m_backgroundBlur->GetBlurredFrame();
                        blurPtr = reinterpret_cast<const UINT32*>( blurFrame.data() );

                        // Verify blur frame size matches expected dimensions.
                        const size_t expectedBlurBytes = static_cast<size_t>(procW) * procH * 4;
                        if( blurFrame.size() != expectedBlurBytes )
                        {
                            OutputDebug( L"[WebcamCapture] BLUR SIZE MISMATCH: "
                                         L"GetBlurredFrame().size()=%zu expected=%zu "
                                         L"proc=%ux%u mask=%ux%u\n",
                                         blurFrame.size(), expectedBlurBytes,
                                         procW, procH, mW, mH );
                        }
                    }
                }

                gpuDone = GpuComposite(
                    srcPixels, m_camWidth, m_camHeight,
                    blurPtr, procW, procH,
                    maskPtr, mW, mH,
                    ovW, ovH,
                    srcCropX, srcCropY, srcCropW, srcCropH,
                    static_cast<float>( m_lutGamma ),
                    m_shape,
                    min( ovW * 0.5f, ovH * 0.5f ) *
                        ( m_shape == RoundedSquare ? 0.40f : 0.10f ),
                    preBlurSRV );

                if( gpuDone )
                {
                    finalW = ovW;
                    finalH = ovH;
                    finalPixels = reinterpret_cast<UINT32*>( m_upscalePixels.data() );
                }
            }

            if( !gpuDone )
            {
                // CPU fallback — same logic as before.
                if( needUpscale && hasMask )
            {
                // Dual-source composite: sharp foreground + blurred background.
                const size_t fullSize = static_cast<size_t>( ovW ) * ovH * 4;
                m_upscalePixels.resize( fullSize );
                UINT32* outBuf = reinterpret_cast<UINT32*>( m_upscalePixels.data() );

                const float* mask = m_backgroundBlur->GetMask().data();
                const uint32_t maskW = m_backgroundBlur->GetMaskWidth();
                const uint32_t maskH = m_backgroundBlur->GetMaskHeight();

                const float halfWOut = ovW * 0.5f;
                const float halfHOut = ovH * 0.5f;
                const float cornerRadiusOut = min( halfWOut, halfHOut ) *
                    ( m_shape == RoundedSquare ? 0.40f : 0.10f );

                for( UINT y = 0; y < ovH; y++ )
                {
                    // Camera bilinear Y setup.
                    const float camYf = srcCropY + ( y + 0.5f ) * srcCropH / static_cast<float>( ovH ) - 0.5f;
                    const float camYfl = floorf( max( 0.0f, camYf ) );
                    const UINT  cy0 = static_cast<UINT>( camYfl );
                    const UINT  cy1 = min( cy0 + 1, m_camHeight - 1 );
                    const float cfy = camYf - camYfl;
                    const float cify = 1.0f - cfy;
                    const UINT32* camRow0 = srcPixels + static_cast<size_t>( cy0 ) * srcW32;
                    const UINT32* camRow1 = srcPixels + static_cast<size_t>( cy1 ) * srcW32;

                    // Blur buffer Y setup (nearest-neighbor for Y — blur hides it).
                    const UINT blurY = min( static_cast<UINT>( ( y + 0.5f ) * procH / static_cast<float>( ovH ) ), procH - 1 );
                    const UINT32* blurRow = reinterpret_cast<const UINT32*>( m_backgroundBlur->GetBlurredFrame().data() )
                                            + static_cast<size_t>( blurY ) * procW;

                    // Mask Y — nearest-neighbor sample.
                    const UINT my = min( static_cast<UINT>( ( y + 0.5f ) * maskH / static_cast<float>( ovH ) ), maskH - 1 );
                    const float* maskRow = mask + static_cast<size_t>( my ) * maskW;

                    UINT32* outRow = outBuf + static_cast<size_t>( y ) * ovW;

                    for( UINT x = 0; x < ovW; x++ )
                    {
                        // Shape mask check (at overlay size).
                        bool inside = true;
                        if( m_shape == Circle )
                        {
                            float radius = min( halfWOut, halfHOut );
                            float dx = ( x + 0.5f - halfWOut ) / radius;
                            float dy = ( y + 0.5f - halfHOut ) / radius;
                            inside = ( dx * dx + dy * dy ) <= 1.0f;
                        }
                        else if( m_shape == RoundedRect || m_shape == RoundedSquare )
                        {
                            float px = x + 0.5f, py = y + 0.5f;
                            float cx2 = 0, cy2 = 0;
                            bool inCorner = false;
                            if( px < cornerRadiusOut && py < cornerRadiusOut )
                            { cx2 = cornerRadiusOut; cy2 = cornerRadiusOut; inCorner = true; }
                            else if( px > ovW - cornerRadiusOut && py < cornerRadiusOut )
                            { cx2 = ovW - cornerRadiusOut; cy2 = cornerRadiusOut; inCorner = true; }
                            else if( px < cornerRadiusOut && py > ovH - cornerRadiusOut )
                            { cx2 = cornerRadiusOut; cy2 = ovH - cornerRadiusOut; inCorner = true; }
                            else if( px > ovW - cornerRadiusOut && py > ovH - cornerRadiusOut )
                            { cx2 = ovW - cornerRadiusOut; cy2 = ovH - cornerRadiusOut; inCorner = true; }
                            if( inCorner )
                            {
                                float ddx2 = px - cx2, ddy2 = py - cy2;
                                inside = ( ddx2 * ddx2 + ddy2 * ddy2 ) <= ( cornerRadiusOut * cornerRadiusOut );
                            }
                        }

                        if( !inside )
                        {
                            outRow[x] = 0x00000000u;
                            continue;
                        }

                        // Mask sample (nearest-neighbor — mask is soft already).
                        const UINT mx = min( static_cast<UINT>( ( x + 0.5f ) * maskW / static_cast<float>( ovW ) ), maskW - 1 );
                        float maskVal = maskRow[mx];

                        UINT32 r, g, b;

                        if( maskVal >= 1.0f )
                        {
                            // Foreground: bilinear from full-res camera.
                            const float camXf = srcCropX + ( x + 0.5f ) * srcCropW / static_cast<float>( ovW ) - 0.5f;
                            const float camXfl = floorf( max( 0.0f, camXf ) );
                            const UINT  cx0 = static_cast<UINT>( camXfl );
                            const UINT  cx1 = min( cx0 + 1, srcW32 - 1 );
                            const float cfx = camXf - camXfl;
                            const float cifx = 1.0f - cfx;

                            UINT32 p00 = camRow0[cx0], p10 = camRow0[cx1];
                            UINT32 p01 = camRow1[cx0], p11 = camRow1[cx1];
                            float w00 = cifx * cify, w10 = cfx * cify;
                            float w01 = cifx * cfy,  w11 = cfx * cfy;

                            b = m_gammaLUT[static_cast<uint8_t>( w00*(p00 & 0xFF) + w10*(p10 & 0xFF) + w01*(p01 & 0xFF) + w11*(p11 & 0xFF) + 0.5f )];
                            g = m_gammaLUT[static_cast<uint8_t>( w00*((p00>>8)&0xFF) + w10*((p10>>8)&0xFF) + w01*((p01>>8)&0xFF) + w11*((p11>>8)&0xFF) + 0.5f )];
                            r = m_gammaLUT[static_cast<uint8_t>( w00*((p00>>16)&0xFF) + w10*((p10>>16)&0xFF) + w01*((p01>>16)&0xFF) + w11*((p11>>16)&0xFF) + 0.5f )];
                        }
                        else if( maskVal <= 0.0f )
                        {
                            // Background: nearest-neighbor from blurred proc buffer
                            // (blur already hides any interpolation artefacts).
                            const UINT bx = min( static_cast<UINT>( ( x + 0.5f ) * procW / static_cast<float>( ovW ) ), procW - 1 );
                            UINT32 px = blurRow[bx];
                            b = ( px       ) & 0xFF;
                            g = ( px >>  8 ) & 0xFF;
                            r = ( px >> 16 ) & 0xFF;
                        }
                        else
                        {
                            // Edge: blend camera foreground with blurred background.
                            const float camXf = srcCropX + ( x + 0.5f ) * srcCropW / static_cast<float>( ovW ) - 0.5f;
                            const float camXfl = floorf( max( 0.0f, camXf ) );
                            const UINT  cx0 = static_cast<UINT>( camXfl );
                            const UINT  cx1 = min( cx0 + 1, srcW32 - 1 );
                            const float cfx = camXf - camXfl;
                            const float cifx = 1.0f - cfx;

                            UINT32 p00 = camRow0[cx0], p10 = camRow0[cx1];
                            UINT32 p01 = camRow1[cx0], p11 = camRow1[cx1];
                            float w00 = cifx * cify, w10 = cfx * cify;
                            float w01 = cifx * cfy,  w11 = cfx * cfy;

                            UINT32 fb = m_gammaLUT[static_cast<uint8_t>( w00*(p00 & 0xFF) + w10*(p10 & 0xFF) + w01*(p01 & 0xFF) + w11*(p11 & 0xFF) + 0.5f )];
                            UINT32 fg = m_gammaLUT[static_cast<uint8_t>( w00*((p00>>8)&0xFF) + w10*((p10>>8)&0xFF) + w01*((p01>>8)&0xFF) + w11*((p11>>8)&0xFF) + 0.5f )];
                            UINT32 fr = m_gammaLUT[static_cast<uint8_t>( w00*((p00>>16)&0xFF) + w10*((p10>>16)&0xFF) + w01*((p01>>16)&0xFF) + w11*((p11>>16)&0xFF) + 0.5f )];

                            const UINT bx = min( static_cast<UINT>( ( x + 0.5f ) * procW / static_cast<float>( ovW ) ), procW - 1 );
                            UINT32 bp = blurRow[bx];
                            UINT32 bb = ( bp       ) & 0xFF;
                            UINT32 bg2 = ( bp >>  8 ) & 0xFF;
                            UINT32 br = ( bp >> 16 ) & 0xFF;

                            float inv = 1.0f - maskVal;
                            r = static_cast<UINT32>( fr * maskVal + br * inv + 0.5f );
                            g = static_cast<UINT32>( fg * maskVal + bg2 * inv + 0.5f );
                            b = static_cast<UINT32>( fb * maskVal + bb * inv + 0.5f );
                        }

                        outRow[x] = 0xFF000000u | ( r << 16 ) | ( g << 8 ) | b;
                    }
                }
                finalW = ovW;
                finalH = ovH;
                finalPixels = outBuf;
            }
            else if( needUpscale )
            {
                // No mask available — simple bilinear upscale of proc buffer.
                const size_t fullSize = static_cast<size_t>( ovW ) * ovH * 4;
                m_upscalePixels.resize( fullSize );
                UINT32* dst = reinterpret_cast<UINT32*>( m_upscalePixels.data() );
                const UINT32* src = reinterpret_cast<const UINT32*>( m_scaledPixels.data() );
                for( UINT y = 0; y < ovH; y++ )
                {
                    const UINT sy = min( static_cast<UINT>( ( y + 0.5f ) * procH / static_cast<float>( ovH ) ), procH - 1 );
                    const UINT32* srcRow = src + static_cast<size_t>( sy ) * procW;
                    UINT32* dstRow = dst + static_cast<size_t>( y ) * ovW;
                    for( UINT x = 0; x < ovW; x++ )
                        dstRow[x] = srcRow[min( static_cast<UINT>( ( x + 0.5f ) * procW / static_cast<float>( ovW ) ), procW - 1 )];
                }
                finalW = ovW;
                finalH = ovH;
                finalPixels = dst;
            }
            else
            {
                // No upscale needed — shape mask at proc resolution.
                if( m_shape != Square )
                for( UINT y = 0; y < procH; y++ )
                {
                    UINT32* dstRow2 = dstPixels + static_cast<size_t>( y ) * procW;
                    for( UINT x = 0; x < procW; x++ )
                    {
                        bool inside = true;
                        if( m_shape == Circle )
                        {
                            float radius = min( halfW, halfH );
                            float dx = ( x + 0.5f - halfW ) / radius;
                            float dy = ( y + 0.5f - halfH ) / radius;
                            inside = ( dx * dx + dy * dy ) <= 1.0f;
                        }
                        else if( m_shape == RoundedRect || m_shape == RoundedSquare )
                        {
                            float px = x + 0.5f, py = y + 0.5f;
                            float cx2 = 0, cy2 = 0;
                            bool inCorner = false;
                            if( px < cornerRadius && py < cornerRadius )
                            { cx2 = cornerRadius; cy2 = cornerRadius; inCorner = true; }
                            else if( px > procW - cornerRadius && py < cornerRadius )
                            { cx2 = procW - cornerRadius; cy2 = cornerRadius; inCorner = true; }
                            else if( px < cornerRadius && py > procH - cornerRadius )
                            { cx2 = cornerRadius; cy2 = procH - cornerRadius; inCorner = true; }
                            else if( px > procW - cornerRadius && py > procH - cornerRadius )
                            { cx2 = procW - cornerRadius; cy2 = procH - cornerRadius; inCorner = true; }
                            if( inCorner )
                            {
                                float ddx2 = px - cx2, ddy2 = py - cy2;
                                inside = ( ddx2 * ddx2 + ddy2 * ddy2 ) <= ( cornerRadius * cornerRadius );
                            }
                        }
                        if( !inside )
                            dstRow2[x] = 0x00000000u;
                    }
                }
                finalW = procW;
                finalH = procH;
                finalPixels = dstPixels;
            }
            } // end if( !gpuDone )

            {
                std::lock_guard<std::mutex> lock( m_frameLock );

#if _DEBUG
                LARGE_INTEGER afterScale;
                QueryPerformanceCounter( &afterScale );
                double scaleMs = static_cast<double>( afterScale.QuadPart - afterCopy.QuadPart ) * 1000.0 / perfFreq.QuadPart;
                totalScaleMs += scaleMs;
#endif

                if( finalPixels == reinterpret_cast<UINT32*>( m_upscalePixels.data() ) )
                    m_pendingPixels.swap( m_upscalePixels );
                else
                    m_pendingPixels.swap( m_scaledPixels );
                m_pendingW = finalW;
                m_pendingH = finalH;
                m_newFrameReady = true;
                frameCount++;

#if _DEBUG
                LARGE_INTEGER afterLock;
                QueryPerformanceCounter( &afterLock );
                double lockMs = static_cast<double>( afterLock.QuadPart - afterScale.QuadPart ) * 1000.0 / perfFreq.QuadPart;
                totalLockMs += lockMs;

                double frameIntervalMs = static_cast<double>( afterLock.QuadPart - lastFrameTime.QuadPart ) * 1000.0 / perfFreq.QuadPart;
                lastFrameTime = afterLock;

                if( frameCount <= 5 || ( frameCount % 30 ) == 0 )
                {
                    OutputDebug( L"[WebcamCapture] frame %d: cam=%ux%u proc=%ux%u overlay=%ux%u "
                                L"read=%.1fms copy=%.1fms scale=%.1fms lock=%.1fms "
                                L"interval=%.1fms avgRead=%.1f avgCopy=%.1f avgScale=%.1f\n",
                                 frameCount, m_camWidth, m_camHeight, procW, procH, ovW, ovH,
                                 readMs, copyMs, scaleMs, lockMs, frameIntervalMs,
                                 totalReadMs / frameCount, totalCopyMs / frameCount,
                                 totalScaleMs / frameCount );
                }
#endif
            }

            // Signal that the first frame is ready so Start() can unblock.
            if( frameCount == 1 )
            {
                std::lock_guard<std::mutex> readyLock( m_readyMutex );
                m_firstFrameCaptured = true;
                m_readyCV.notify_one();
            }
        }

    }

    // Release MF objects on the thread that created them.
    m_sourceReader = nullptr;
    if( m_mfStarted )
    {
        MFShutdown();
        m_mfStarted = false;
    }

    CoUninitialize();
}

//----------------------------------------------------------------------------
// WebcamCapture::ComputeDestRect
//
// Computes the destination rectangle for the webcam overlay on the
// recording output surface.
//----------------------------------------------------------------------------
RECT WebcamCapture::ComputeDestRect() const
{
    // Full screen: overlay covers the entire output area (no edges).
    if( m_size == FullScreen )
    {
        // In fullscreen recording mode, letterbox/pillarbox the webcam
        // to preserve its full field of view without magnification.
        if( m_fullScreenRecording && m_camWidth > 0 && m_camHeight > 0 )
        {
            double camAspect = static_cast<double>( m_camWidth ) / m_camHeight;
            double outAspect = static_cast<double>( m_outputWidth ) / m_outputHeight;
            LONG fitW, fitH;
            if( camAspect > outAspect )
            {
                // Camera is wider — fit to width, pillarbox top/bottom.
                fitW = static_cast<LONG>( m_outputWidth );
                fitH = static_cast<LONG>( m_outputWidth / camAspect + 0.5 );
            }
            else
            {
                // Camera is taller — fit to height, letterbox sides.
                fitH = static_cast<LONG>( m_outputHeight );
                fitW = static_cast<LONG>( m_outputHeight * camAspect + 0.5 );
            }
            LONG x = ( static_cast<LONG>( m_outputWidth ) - fitW ) / 2;
            LONG y = ( static_cast<LONG>( m_outputHeight ) - fitH ) / 2;
            return RECT{ x, y, x + fitW, y + fitH };
        }

        return RECT{ 0, 0,
                     static_cast<LONG>( m_outputWidth ),
                     static_cast<LONG>( m_outputHeight ) };
    }

    // Size percentages: Small=15%, Medium=25%, Large=33%, XLarge=50%.
    static const int sizePercent[] = { 15, 25, 33, 50 };
    const int pct = sizePercent[min( static_cast<int>( m_size ), 3 )];
    const int margin = 8;

    // Compute overlay dimensions maintaining camera aspect ratio.
    int overlayW = static_cast<int>( m_outputWidth ) * pct / 100;
    int overlayH = ( m_camHeight > 0 && m_camWidth > 0 )
        ? overlayW * static_cast<int>( m_camHeight ) / static_cast<int>( m_camWidth )
        : overlayW * 3 / 4;

    // Clamp to output size.
    if( overlayW > static_cast<int>( m_outputWidth ) - margin * 2 )
        overlayW = static_cast<int>( m_outputWidth ) - margin * 2;
    if( overlayH > static_cast<int>( m_outputHeight ) - margin * 2 )
        overlayH = static_cast<int>( m_outputHeight ) - margin * 2;

    // For circle and rounded-square shapes, make the bounding box square
    // (use the smaller dimension).  The webcam source is center-cropped
    // to fill this square.
    if( m_shape == Circle || m_shape == RoundedSquare )
    {
        int diameter = min( overlayW, overlayH );
        overlayW = diameter;
        overlayH = diameter;
    }

    RECT dst = {};
    switch( m_position )
    {
    case TopLeft:
        dst.left = margin;
        dst.top = margin;
        break;
    case TopRight:
        dst.left = static_cast<int>( m_outputWidth ) - overlayW - margin;
        dst.top = margin;
        break;
    case BottomLeft:
        dst.left = margin;
        dst.top = static_cast<int>( m_outputHeight ) - overlayH - margin;
        break;
    case BottomRight:
    default:
        dst.left = static_cast<int>( m_outputWidth ) - overlayW - margin;
        dst.top = static_cast<int>( m_outputHeight ) - overlayH - margin;
        break;
    }
    dst.right = dst.left + overlayW;
    dst.bottom = dst.top + overlayH;
    return dst;
}

//----------------------------------------------------------------------------
// WebcamCapture::ComputeOverlayDimensions
//
// Pre-computes the overlay width/height and destination rect once the
// camera resolution is known.
//----------------------------------------------------------------------------
void WebcamCapture::ComputeOverlayDimensions()
{
    m_destRect = ComputeDestRect();
    m_overlayW = static_cast<UINT>( max( 1L, m_destRect.right - m_destRect.left ) );
    m_overlayH = static_cast<UINT>( max( 1L, m_destRect.bottom - m_destRect.top ) );
}

//----------------------------------------------------------------------------
// WebcamCapture::CompositeOnto
//
// Composites the pre-scaled webcam overlay onto the target texture.
// When a new webcam frame arrives, create a fresh GPU texture from the
// CPU pixels using initial subresource data, then copy that texture into
// the target surface. This is a conservative path that avoids both the
// UpdateSubresource and Map/Unmap upload behaviors that appear to vary
// across desktop Intel driver stacks.
//----------------------------------------------------------------------------
bool WebcamCapture::CompositeOnto( ID3D11Texture2D* target )
{
    if( !m_enabled.load( std::memory_order_relaxed ) )
        return false;

#if _DEBUG
    m_compositeCount++;
#endif

    // If the capture thread has new pixels, upload them to the cached
    // GPU texture.  Use try_lock so we NEVER block the encoder callback.
    // If the lock is contended, just reuse the previous cached texture.
    if( m_frameLock.try_lock() )
    {
        if( m_newFrameReady && !m_pendingPixels.empty() && m_pendingW > 0 && m_pendingH > 0 )
        {
#if _DEBUG
            m_uploadCount++;
#endif

            // Use the dimensions the pixels were actually produced at,
            // NOT m_overlayW/m_overlayH which may have been changed by
            // SetDestRectAndSize since the capture thread produced this frame.
            const UINT pixW = m_pendingW;
            const UINT pixH = m_pendingH;

            // Recreate the texture if dimensions changed.
            if( m_texW != pixW || m_texH != pixH )
            {
                m_overlayTex = nullptr;

                D3D11_TEXTURE2D_DESC texDesc = {};
                texDesc.Width = pixW;
                texDesc.Height = pixH;
                texDesc.MipLevels = 1;
                texDesc.ArraySize = 1;
                texDesc.Format = DXGI_FORMAT_B8G8R8A8_UNORM;
                texDesc.SampleDesc.Count = 1;
                texDesc.Usage = D3D11_USAGE_DEFAULT;
                texDesc.BindFlags = 0;

                if( FAILED( m_d3dDevice->CreateTexture2D( &texDesc, nullptr, m_overlayTex.put() ) ) )
                {
                    m_overlayTex = nullptr;
                    m_newFrameReady = false;
                    m_frameLock.unlock();
                    return false;
                }
                m_texW = pixW;
                m_texH = pixH;
            }

            // Upload new pixels.  UpdateSubresource + CopySubresourceRegion
            // on the same immediate context are serialized by the runtime,
            // so there is no read-write hazard.
            m_d3dContext->UpdateSubresource(
                m_overlayTex.get(), 0, nullptr,
                m_pendingPixels.data(), pixW * 4, 0 );

            // Keep a CPU copy for alpha-blended compositing (shaped overlays).
            if( m_shape != Square )
                m_lastUploadedPixels = m_pendingPixels;

            m_hasOverlay = true;
            m_newFrameReady = false;
        }
        m_frameLock.unlock();
    }
    else
    {
#if _DEBUG
        m_lockFailCount++;
#endif
    }

#if _DEBUG
    if( ( m_compositeCount % 30 ) == 0 )
    {
        OutputDebug( L"[WebcamCapture] Composite: calls=%d uploads=%d lockFails=%d hasOverlay=%d\n",
                     m_compositeCount, m_uploadCount, m_lockFailCount, m_hasOverlay ? 1 : 0 );
    }
#endif

    if( !m_hasOverlay )
        return false;

    // For Square shape, use the fast GPU-only blit (no alpha needed).
    // For RoundedRect/Circle, we need alpha blending.
    if( m_shape == Square )
    {
        D3D11_TEXTURE2D_DESC targetDesc = {};
        target->GetDesc( &targetDesc );

        UINT destX = static_cast<UINT>( max( 0L, m_destRect.left ) );
        UINT destY = static_cast<UINT>( max( 0L, m_destRect.top ) );

        D3D11_BOX srcBox = {};
        srcBox.right = min( m_texW, targetDesc.Width - destX );
        srcBox.bottom = min( m_texH, targetDesc.Height - destY );
        srcBox.back = 1;

        m_d3dContext->CopySubresourceRegion(
            target, 0, destX, destY, 0,
            m_overlayTex.get(), 0, &srcBox );
    }
    else
    {
        // Alpha-blended compositing for shaped overlays.
        // Read the target region into a staging texture, blend overlay
        // pixels on CPU, then copy back. The overlay is small (typically
        // 200-400px wide) so this is fast.
        D3D11_TEXTURE2D_DESC targetDesc = {};
        target->GetDesc( &targetDesc );

        UINT destX = static_cast<UINT>( max( 0L, m_destRect.left ) );
        UINT destY = static_cast<UINT>( max( 0L, m_destRect.top ) );
        UINT copyW = min( m_texW, targetDesc.Width - destX );
        UINT copyH = min( m_texH, targetDesc.Height - destY );

        if( copyW == 0 || copyH == 0 )
            return false;

        // Create or reuse staging textures for readback/upload.
        if( !m_stagingTex || m_stagingW != copyW || m_stagingH != copyH )
        {
            m_stagingTex = nullptr;
            D3D11_TEXTURE2D_DESC stagingDesc = {};
            stagingDesc.Width = copyW;
            stagingDesc.Height = copyH;
            stagingDesc.MipLevels = 1;
            stagingDesc.ArraySize = 1;
            stagingDesc.Format = DXGI_FORMAT_B8G8R8A8_UNORM;
            stagingDesc.SampleDesc.Count = 1;
            stagingDesc.Usage = D3D11_USAGE_STAGING;
            stagingDesc.CPUAccessFlags = D3D11_CPU_ACCESS_READ | D3D11_CPU_ACCESS_WRITE;
            if( FAILED( m_d3dDevice->CreateTexture2D( &stagingDesc, nullptr, m_stagingTex.put() ) ) )
                return false;
            m_stagingW = copyW;
            m_stagingH = copyH;
        }

        // Copy the target region to staging for readback.
        D3D11_BOX targetBox = {};
        targetBox.left = destX;
        targetBox.top = destY;
        targetBox.right = destX + copyW;
        targetBox.bottom = destY + copyH;
        targetBox.back = 1;
        m_d3dContext->CopySubresourceRegion( m_stagingTex.get(), 0, 0, 0, 0,
                                             target, 0, &targetBox );

        // Map staging, blend overlay pixels.
        D3D11_MAPPED_SUBRESOURCE mapped = {};
        if( SUCCEEDED( m_d3dContext->Map( m_stagingTex.get(), 0, D3D11_MAP_READ_WRITE, 0, &mapped ) ) )
        {
            // Also need overlay pixels. Map the overlay texture via a
            // separate staging texture, or use the cached CPU pixels.
            // Using the cached CPU pixels (m_pendingPixels) is simplest.
            // But m_pendingPixels may have been swapped — use a lock-free
            // copy stored during upload.
            // Actually, the overlay texture already has the data. We can
            // copy it to another staging texture and map it. But simpler:
            // keep a CPU copy of the last-uploaded pixels.
            const UINT32* overlayPixels = reinterpret_cast<const UINT32*>( m_lastUploadedPixels.data() );
            if( !m_lastUploadedPixels.empty() )
            {
                for( UINT row = 0; row < copyH; row++ )
                {
                    UINT32* dstRow = reinterpret_cast<UINT32*>(
                        static_cast<BYTE*>( mapped.pData ) + row * mapped.RowPitch );
                    const UINT32* srcRow = overlayPixels + row * m_texW;

                    for( UINT col = 0; col < copyW; col++ )
                    {
                        UINT32 ovPixel = srcRow[col];
                        UINT32 ovAlpha = ( ovPixel >> 24 ) & 0xFF;
                        if( ovAlpha == 0xFF )
                        {
                            dstRow[col] = ovPixel;
                        }
                        else if( ovAlpha > 0 )
                        {
                            // Alpha blend: dst = src * alpha + dst * (1-alpha)
                            UINT32 bg = dstRow[col];
                            UINT32 invAlpha = 255 - ovAlpha;
                            UINT32 r = ( ( ( ovPixel >> 16 ) & 0xFF ) * ovAlpha + ( ( bg >> 16 ) & 0xFF ) * invAlpha ) / 255;
                            UINT32 g = ( ( ( ovPixel >> 8 ) & 0xFF ) * ovAlpha + ( ( bg >> 8 ) & 0xFF ) * invAlpha ) / 255;
                            UINT32 b = ( ( ovPixel & 0xFF ) * ovAlpha + ( bg & 0xFF ) * invAlpha ) / 255;
                            dstRow[col] = 0xFF000000u | ( r << 16 ) | ( g << 8 ) | b;
                        }
                        // ovAlpha == 0: keep background pixel unchanged.
                    }
                }
            }
            m_d3dContext->Unmap( m_stagingTex.get(), 0 );

            // Copy blended staging back to the target.
            D3D11_BOX stagingBox = {};
            stagingBox.right = copyW;
            stagingBox.bottom = copyH;
            stagingBox.back = 1;
            m_d3dContext->CopySubresourceRegion( target, 0, destX, destY, 0,
                                                 m_stagingTex.get(), 0, &stagingBox );
        }
    }

    return true;
}

//----------------------------------------------------------------------------
// WebcamCapture::GetLatestPixels
//
// Copies the latest pre-scaled BGRA pixels for on-screen preview.
// Thread-safe — acquires m_frameLock to read m_pendingPixels.
//----------------------------------------------------------------------------
bool WebcamCapture::GetLatestPixels( std::vector<BYTE>& outPixels, UINT& outW, UINT& outH )
{
    if( !m_enabled.load( std::memory_order_relaxed ) )
        return false;

    std::lock_guard<std::mutex> lock( m_frameLock );
    if( m_pendingPixels.empty() || m_pendingW == 0 || m_pendingH == 0 )
        return false;

    outPixels = m_pendingPixels;
    outW = m_pendingW;
    outH = m_pendingH;
    return true;
}
